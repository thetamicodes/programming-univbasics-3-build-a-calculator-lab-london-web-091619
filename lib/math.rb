def addition(num1, num2)
result = num1 + num2
return result
end

def subtraction(num1, num2)
result = num1 - num2
return result
end

def division(num1, num2)
result = num1 / num2
return result
end

def multiplication(num1, num2)
result = num1 * num2
return result
end

def modulo(num1, num2)
result = num1 % num2
return result
end

def square_root(num)
require 'cmath'
result = CMath.sqrt(num)
return result
end
